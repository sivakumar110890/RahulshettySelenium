package arrayExample;

public class Array2 {

	public static void main(String[] args) {
		int nums[]= {1,2,3,4};
		System.out.println("Before replacing the value of 0th index : "+nums[0]);
		nums[0]=5; // Here we are replacing the array value
		System.out.println("After replacing the value of 0th index : "+nums[0]);
	}

}
