package javaBasics;

public class javaVariablesDataTypes {
	public static void main(String[] args) {
		int num=1;
		char letter='a';
		String name="Sivakumar";
		float decimal=(float) 4.9;
		double doublle=45.99;
		boolean tru= true;
		System.out.println("Variable integer Value  is : "+num);
		System.out.println("Variable char Value  is : "+letter);
		System.out.println("Variable String Value  is : "+name);
		System.out.println("Variable float Value  is : "+decimal);
		System.out.println("Variable double Value  is : "+doublle);
		System.out.println("Variable Boolean Value  is : "+tru);
		
		
	}

}
